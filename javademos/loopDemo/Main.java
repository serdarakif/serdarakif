package javademos.loopDemo;


public class Main 
{
    public static void main(String[] args)
     {   
      for( int i=1;i<10;i++)
      {
        System.out.println(i);
      } 
      for( int i=1;i<20;i+=2)
      {
        System.out.println(i);
      } 
      //While
      int i=0;
      while (i<20)
      {
        i++;
        System.out.println(i);
      }
      System.out.println(i);
     //Ders13  
     }
        
    }
